package ex_10_For_Loop;

public class Lab101_forloop_with_if {
    public static void main(String[] args) {
        for (int i = 0; i < 18; i++) {
            if (i > 15){
                System.out.println("gift");
            }else {
                System.out.println("no gift");
            }

        }
    }
}
