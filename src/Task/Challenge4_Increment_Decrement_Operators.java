package Task;

public class Challenge4_Increment_Decrement_Operators {
    public static void main(String[] args){
        int a = 5;
        System.out.println("Original: " + a);
        System.out.println("Original: " + (++a));
        System.out.println("Original: " + (a++));
        System.out.println("Original: " + (--a));
        System.out.println("Original: " + (a--));





    }


}
